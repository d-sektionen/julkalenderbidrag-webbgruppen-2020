export function Enemy(ctx, width, height) {

    console.log("Im constructred")

    this.x;//∂
    this.y;
    this.radius = 50;
    // delta x and y, the x and y pixels that the enemy should move
    this.dx;
    this.dy;



    this.pickLocation = () => {
        var rnd = Math.floor(Math.random() * 4);
        switch (rnd) {
            case 0:
                this.x = 0;
                this.y = Math.random() * height;
                break;
            case 1:
                this.x = width;
                this.y = Math.random() * height;
                break;
            case 2:
                this.x = Math.random() * width;
                this.y = 0;
                break;
            case 3:
                this.x = Math.random() * width;
                this.y = height;
                break;
        }
        this.x = Math.floor(this.x) - this.radius / 2;
        this.y = Math.floor(this.y) - this.radius / 2;
        // console.log("Spawned enemy at", this.x, this.y);
    }


    
    this.update = () => 
    {
        console.log("UPDATIN'");

        ctx.fillStyle = "red";
        ctx.fillRect(this.x, this.y, 50, 50);
    }

    this.remove = () =>
    {
        ctx.clearRect(this.x, this.y, 50, 50);
    }


    this.pickLocation();



    this.move = () =>
    {
        this.remove();

        this.x += 10;
        this.y += 10;

        ctx.fillStyle = "red";
        ctx.fillRect(this.x, this.y, 50, 50);
    }






}

